# Example of a lambda function # A lambda function is an anonymous function that takes in arguments and
# returns a value. They are often used for quick, one-off calculations.
# They can be used as a replacement for a small, one-off function.

# Calculate the area of a rectangle using a lambda function
area = lambda l, w: l * w
print("The area of a rectangle with length {} and width {} is {}".format(5, 3, area(5, 3)))

# Calculate the sum of two numbers using a lambda function
add = lambda x, y: x + y
print("The sum of {} and {} is {}".format(4, 5, add(4, 5)))
